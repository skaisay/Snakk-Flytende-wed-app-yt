function findConversationResponse(text) {
    const normalizedText = text.toLowerCase().trim();
    
    for (const item of CONVERSATION_DATABASE) {
        if (item.keywords.some(keyword => normalizedText.includes(keyword))) {
            return item.answer;
        }
    }
    
    return null;
}

