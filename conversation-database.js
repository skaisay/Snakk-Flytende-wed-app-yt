function getRandomConversationTip() {
    const tips = CONVERSATION_DATABASE.filter(item => 
        item.category === 'facts' || item.category === 'motivation'
    );
    const randomTip = tips[Math.floor(Math.random() * tips.length)];
    return randomTip ? randomTip.answer : null;
}

